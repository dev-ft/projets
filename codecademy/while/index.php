Mais comment faire lorsque vous ne savez pas, à l'avance, combien de fois la boucle doit se répéter ? Dans ce cas, vous pouvez utiliser une boucle while.

La boucle while s'exécutera tant qu'une certaine condition est vraie. 
<hr>

<?php
	$maLigne = 0;

	while($maLigne < 10){
		$maLigne++;
		echo "<br>"."ma ligne"." $maLigne";
	}












?>